"""개조식 변환 프롬프트 빌더.

규칙은 한국 연구 발표 슬라이드 코퍼스 분석으로 도출·보정됨(방법론: docs/methodology.md).
핵심:
  - 디폴트 종결은 '순수 명사 종결'. '-ㅁ/음'은 완결 서술절·저자판단에 한정.
  - '-기'는 종결 어미로 쓰지 않음('-기 위해' 연결만).
  - 조사 생략은 무조건이 아님 → 명사구화 가능 시만 생략.
  - 제목은 라벨형(명사구). 주장은 본문 글머리표로.
  - 영어 전문용어는 원어 유지(강제 번역 금지), '영어 명사구 + 한국어 조사/술어' 골격.
  - 군더더기 일괄삭제 금지(대조/전환 논리 유지), 인과·귀결은 →/∝, 라벨-정의는 : 로 외주화.

few-shot 예시(data/fewshot_pairs.json)는 모두 독창적 합성 데이터다(특정 발표 전사본 아님).
"""
from __future__ import annotations

import json
import re
from functools import lru_cache
from importlib import resources

# 코퍼스 분석으로 도출한 변환 규칙(시스템 프롬프트 본문)
RULESET = """너는 한국 학술 연구 발표 PPT의 "개조식(箇條式)" 문체 교정기다.
입력으로 들어온 구어체/완전문장체 한국어 슬라이드 텍스트를, 한국 연구실 발표 슬라이드에서 실제로 쓰이는 개조식으로 다듬어라.

[최우선 원칙]
- 의미·수치·인과관계·고유명사를 보존한다. 내용을 더하거나 빼지 않는다(문체만 변환).
- 근사·정도·역접 뉘앙스('약', '정도', '오히려', '다만')도 의미의 일부이므로 함부로 떨어뜨리지 않는다.
- 비문을 만들지 않는다. 과잉 압축으로 뜻이 모호해지면 압축을 멈춘다.

[종결 양식] (코퍼스 분포: 명사 ≈ 75% · -ㅁ/음 ≈ 24% · -기 0% · 완전문장 ≈ 2%)
- 기본값은 **순수 명사 종결**: 명사구만 제시할 때는 체언으로 끝낸다. 예) "성능 향상", "한계 존재", "Patch Feature 추출".
- **-ㅁ/음 종결**은 (a) 주어·부사어를 거느린 완결된 서술절이거나 (b) 저자의 수행·판단·당위를 진술할 때만 쓴다. 예) "제안함", "필요함", "보여줌", "달라짐".
- **'-기'를 종결 어미로 쓰지 않는다**(코퍼스 0건). '-기'는 '측정하기 위해' 같은 연결로만 등장.
- 완전문장(~다/~이다/~한다/~는가?/~하자)은 쓰지 않는다. 단, 연구질문(RQ) 제시·인사말은 예외로 허용.
- 같은 부모 아래 형제 항목들은 종결 양식을 일치시킨다(명사면 명사끼리, -ㅁ음이면 -ㅁ음끼리).

[압축]
- 용언 활용 어미를 잘라 명사/명사형으로 종결한다(이것이 1차 압축).
- 조사 생략은 **조건부**다: [명사+동사명사형] 복합어로 명사구화할 수 있으면 조사를 떨군다("최대 batch 사용", "misalignment 보정"). 한 줄에 절차·인과를 서술해야 하면 목적격조사를 살린다("PCA를 도입", "단어를 추출").
- 주격조사는 '명사 + 서술명사(존재/발생/저하/향상/필요)' 결합으로 생략한다("성능 차이 발생", "후처리 필요").
- 군더더기를 줄이되 **일괄 삭제하지 않는다**: 대조·전환·인과 논리(그러나/다만/반면)는 의미상 필요하면 남긴다.
- 인과·비례·귀결은 화살표(→, ⇒, ∝)로, 라벨-정의 대응은 콜론(:)으로 외주화한다("파라미터 ∝ 성능", "Stepwise: N개 layer 선택").

[어휘]
- 영어 전문용어는 원어 그대로 유지한다(LoRA, transformer, threshold, outlier…). 정착 한자어가 있을 때만 번역(가중치, 분산, 처리량). 강제 번역 금지.
- 골격은 '영어 명사구 + 한국어 조사/술어'다(rank가, block을, batch를). 영어 명사 받침 유무에 맞춰 조사를 붙인다(은/는, 이/가, 을/를).
- 추상·기술 개념은 한자어 명사를 선호한다. 능동/수동을 따지기보다 명사화를 우선한다.

[위계·길이]
- 한 줄은 공백 어절 약 7개(상한 ~25토큰, 글자 약 30자) 안으로. 길면 위계를 한 단계 내리거나 콜론/화살표로 한 줄 2단 압축.
- 위계는 최대 3단계까지 허용(상위=짧은 라벨, 말단=설명·조건으로 길어져도 됨).

[제목]
- 슬라이드 제목은 **라벨형 명사구**로 둔다(종결 어미 금지). 예) "제안 방법", "실험 결과".
- 주장(평서문)은 제목이 아니라 본문 글머리표로 내린다.

[안티패턴 교정]
- 완전문장(해라체/의문문/청유형/음슴체+마침표 남용)은 명사형/명사 종결로 정규화.
- "Gemma: 10%p performance"처럼 증감 방향이 빠진 단편에는 향상/저하 같은 방향 술어를 보강.
- 화살표가 인과·결과·예시·대안을 한꺼번에 떠안아 모호하면 의미를 명시(또는 콜론/술어로 보강).

출력은 다듬은 개조식 텍스트만 낸다(설명·머리말 없이). 입력의 줄/불릿 구조는 보존한다."""

_UNIT_NOTES = {
    "title": "제목 한 줄을 라벨형 명사구로 다듬어라(종결 어미 금지).",
    "bullet": "각 줄(불릿)을 개조식으로 다듬어라. 줄 수와 위계를 보존하라.",
    "slide": "슬라이드 전체 텍스트를 개조식으로 다듬어라. 제목은 라벨형, 본문은 명사/명사형 종결로.",
}


@lru_cache(maxsize=1)
def load_pairs() -> list:
    """패키지에 동봉된 합성 few-shot 변환쌍을 로드한다."""
    raw = resources.files("gaejo.data").joinpath("fewshot_pairs.json").read_text("utf-8")
    return json.loads(raw)


def select_fewshot(n_per_type: int = 5) -> list:
    """종결 양식별로 다양하게 few-shot을 고른다(기호/콜론/핵심어전치 우선 포함)."""
    pairs = load_pairs()

    def priority(p):
        s = 0
        if re.search(r"[→⇒∝:]", p["target"]):
            s += 2
        if "핵심어전치" in p.get("rules", []):
            s += 1
        return -s  # 우선순위 높은 것 먼저

    chosen = []
    for ending in ("명사", "ㅁ음"):
        cands = sorted([p for p in pairs if p["ending"] == ending], key=priority)
        chosen.extend(cands[:n_per_type])
    return chosen


def _format_fewshot(pairs) -> str:
    return "\n\n".join(f"입력: {p['source']}\n출력: {p['target']}" for p in pairs)


def build_messages(text: str, unit: str = "bullet", n_per_type: int = 5):
    """Anthropic Messages 형식의 (system, user)를 만든다.

    few-shot은 필수다(순수 zero-shot은 무효 응답률이 높음 — Reif et al., ACL 2022).
    """
    fewshot = _format_fewshot(select_fewshot(n_per_type))
    unit_note = _UNIT_NOTES.get(unit, _UNIT_NOTES["bullet"])
    user = f"[예시]\n{fewshot}\n\n[작업] {unit_note}\n\n입력:\n{text}\n출력:"
    return RULESET, user
